                            <h2 class="mb-3">Akagera National Park | General Information</h2>
                            <p>Akagera National Park is located in the north east of Rwanda along the border with Tanzania.
                                It is named after the Akagera River that flows along its eastern boundary and feeds into a labyrinth of lakes of which the largest is Lake Ihema.
                                The forest-fringed lakes, papyrus swamps, savannah plains, and rolling highlands combine to make Akagera among the most scenic reserves anywhere in Africa.</p><br>
                                <h4>Day 1: Transfer from Airport to the Hotel</h4> <br>
                                <p class="mb-2">On your safari day, you guide will meet you at your hotel in Kigali at 5:30 am, share a short briefing before you drive off to Akagera national park, which is located 2 hours away, close to the Tanzania border in the Easter side of the country. You will have a great en route drive, with stopovers along the way. By early 8am, you will be at the park gate, register and kick off, your game drive hunt, in search for the wildlife such as buffaloes, impalas, elephants, zebras, giraffes and much more. You drive will stretch to the northern side of the park, where much concentration of wildlife is sighted. A picnic lunch will be enjoyed during the course of the say, as you proceed with the game drive. As time pas by, at around evening ours, you will exit the park in the northern gate, and drive back to Kigali, reaching in the evening, and dropped at your hotel in Kigali </p>

                                
            
            <h4>Day 2: Akagera Park Adventure Tour</h4> <br>
            <p class="mb-3">Have breakfast at your hotel in Kigali, meet your guide, share a short briefing before you drive off about 110km to Akagera , which roughly 2.5 hour’s drive from Kigali,  with a first visit to the largest protected wetland in central Africa. Notable plains game include elephant, buffalo, topi, zebra, waterbuck, eland and different types of antelope. Of the primates, olive baboons, vervets and the secretive blue monkey are seen during the day, with bushbabies often seen on night drives.</p>

